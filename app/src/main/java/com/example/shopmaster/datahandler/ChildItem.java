package com.example.shopmaster.datahandler;

public class ChildItem {

    // Declaration of the variable
    private String ChildItemName;
    private String ChildItemPrice;
    private String ChildItemImgUrl;

    public ChildItem() {

    }

    public String getChildItemName() {
        return ChildItemName;
    }

    public void setChildItemName(String childItemName) {
        ChildItemName = childItemName;
    }

    public String getChildItemPrice() {
        return ChildItemPrice;
    }

    public void setChildItemPrice(String childItemPrice) {
        ChildItemPrice = childItemPrice;
    }

    public String getChildItemImgUrl() {
        return ChildItemImgUrl;
    }

    public void setChildItemImgUrl(String childItemImgUrl) {
        ChildItemImgUrl = childItemImgUrl;
    }
}